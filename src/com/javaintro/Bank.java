package com.javaintro;

public class Bank {
	static int accountNo;
	String name;
	int balance;
	{
		accountNo++;
	}
	void method(int accountNo,String name,int balance) {
		System.out.println("AccountNo:"+accountNo);
		System.out.println("Name:"+name);
		System.out.println("Balance:"+balance);
	}
	public static void main(String[] args) {
		Bank b1=new Bank();
		accountNo=100;
		b1.method(accountNo,"harini",1000);
		Bank b2=new Bank();
		b2.method(accountNo,"harini",1000);
		
}
}


































































































































































































































